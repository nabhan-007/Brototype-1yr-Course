#include <stdio.h>

int main()
{
    int age = 20; // Used to store whole numbers which can be negative or positive
    printf("Age: %d\n", age);

    float price = 99.99; // Used to store decimal numbers
    printf("Price: %.2f\n", price);

    char grade = 'A'; // Used to store a single character
    printf("Grade: %c\n", grade);

    return 0;
}